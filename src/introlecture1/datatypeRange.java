package introlecture1;

public class datatypeRange {
    // boolean
    public static void main(String[] args) {
        byte byteRange;
        byteRange=124; // (-128 to 127)
        System.out.println(byteRange);

        short shortRange;
        shortRange=-200; // range -32768 to 32767
        System.out.println(shortRange);

        int intRanges;
        intRanges= -32421;
        System.out.println(intRanges);

        long longRange;
        longRange=-32421;

        double doubleRange=932.32312;
        System.out.println(doubleRange);

        char charRange = 'w';
        System.out.println(charRange);

        String myString="Myself Monu dhakad";
        System.out.println(myString);
    }
}
